企业需要完成三个文档：《问题定义书》——明确需要解决的核心问题，而非模糊的"需要增长"；《成功标准》——定义项目成功的量化指标；《资源地图》——梳理企业内部的可用资源和潜在阻力。

阶段二：能力匹配（入席前1周）

左安门基于需求文档，匹配3位候选高阶人才，安排企业与候选人的深度沟通。匹配的标准不仅是"经验对口"，更要考虑"文化适配度"和"变革推动力"。

阶段三：入席仪式（入席第1天）

正式的项目启动会，核心目标有三个：对齐期望、建立节奏、明确边界。

## 三、入席中：五维协作模型

在项目执行过程中，我们采用五维协作模型来确保项目质量：

维度一：定期节奏。每周一次同步会、每两周一次复盘会、每月一次战略对齐会。

维度二：透明进展。所有工作进展、决策过程和风险点都在共享文档中实时更新。

维度三：快速反馈。建立"48小时反馈机制"——任何问题在48小时内必须得到响应和处理。

维度四：知识沉淀。要求高阶人才在项目过程中同步输出方法论文档，而非仅在项目结束时一次性交付。

维度五：动态调整。允许项目目标和方法在项目中期根据实际情况进行调整，避免"一条路走到黑"。

## 四、入席后：三交付标准

项目结束时，左安门要求三个交付物：

交付一：业务成果。达成或超越预设的成功指标。

交付二：方法论文档。将项目中的经验和方法沉淀为可复制的文档，供企业内部团队持续使用。

交付三：能力转移。确保企业内部的继任团队能够理解并延续已建立的方法和流程。

这三个交付物确保项目制协作的价值不是一次性的，而是可持续的。

## 五、给企业的建议

如果你是第一次尝试项目制高阶协作，我们的建议是：

1. 先从小项目开始。选择一个2-4周的小项目作为试水，验证协作模式是否适合你的组织。

2. 给外部人才足够的空间。项目制协作的核心价值在于外部视角和独立判断，过度约束会降低效果。

3. 内部安排对接人。指定一位内部核心对接人，负责协调资源、推动落地、同步信息。

